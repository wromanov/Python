#Tipo String
# Uma cadeia de caracteres, tudo que estiver entre aspas ou aspas duplas em Python.
print('Alguma Coisa')
print("Outra Coisa")

#Escapando aspas duplas ou aspas simples
print("Essa é uma 'String' (str).")
print('Essa é uma "String" (str).')

#Caracteres de escape de string "\"
print("Essa é uma \"String\" (str).")

#utilizar o 'r" para que o interpretador execute o texto como foi escrito, sem executar nada.
print(r"Esse é meu \n (str)")
